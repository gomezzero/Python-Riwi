# amarillo: alegria
# rojo: color de la sangre
# azul: mar
color = input("ingresa un color que sea amarillo, rojo o azul ").lower()
if (color == "amarillo"):
    print("Es el color de la algria")
elif (color == "rojo"):
    print("Es el color de la sangre")
elif (color == "azul"):
    print("Es el color del mar")
else:
    print("No admitido")